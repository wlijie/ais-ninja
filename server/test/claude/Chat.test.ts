import {Chat} from "../../src/ai/anthropic/Chat";

describe('claude chat', () => {

  it('simple direct chat', async () => {
    const chat = new Chat();
    const completion = await chat.chat({rule: 'user', message: 'hello world'},
      {
        max_tokens_to_sample: 1000,
        model: 'claude-instant-1'
      }
    )
    console.log(completion)
    expect(completion).not.toBeUndefined();
  });
});
